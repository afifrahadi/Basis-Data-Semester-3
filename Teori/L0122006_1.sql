DROP TABLE IF EXISTS [SITASI];
DROP TABLE IF EXISTS [PENULIS_ARTIKEL];
DROP TABLE IF EXISTS [ARTIKEL];
DROP TABLE IF EXISTS [JURNAL];
DROP TABLE IF EXISTS [PENULIS];
DROP TABLE IF EXISTS [INSTITUSI];


CREATE TABLE [INSTITUSI]
(
	[KdInstitusi] INT PRIMARY KEY,
	[Nama] VARCHAR (128) UNIQUE
);

CREATE TABLE [PENULIS]
(
	[KdPenulis] INT PRIMARY KEY,
	[Nama] VARCHAR (128) UNIQUE,
	[Jabatan] VARCHAR (128),
	[KdInstitusi] INT FOREIGN KEY REFERENCES [INSTITUSI] (KdInstitusi)
);

CREATE INDEX [idx_Nama_Penulis] ON [PENULIS](Nama);

CREATE TABLE [JURNAL]
(
	[KdJurnal] INT PRIMARY KEY,
	[Nama] VARCHAR (128) UNIQUE
);
CREATE INDEX [idx_Nama_Jurnal] ON [JURNAL](Nama);

CREATE TABLE [ARTIKEL]
(
	[KdArtikel] INT PRIMARY KEY,
	[KdJurnal] INT FOREIGN KEY REFERENCES [JURNAL] (KdJurnal),
	[Judul] VARCHAR(256) UNIQUE,
	[Abstrak] VARCHAR(256),
	[TglSubmit] DATE,
	[TglPublikasi] DATE,
	[JmlDownload] INT,
	[Doi] VARCHAR(128)
);
CREATE INDEX [idx_KdJurnal_Artikel] ON [ARTIKEL](KdJurnal);
CREATE INDEX [idx_Judul_Artikel] ON [ARTIKEL](Judul);
CREATE INDEX [idx_TglSubmit_Artikel] ON [ARTIKEL](TglSubmit);
CREATE INDEX [idx_TglPublikasi_Artikel] ON [ARTIKEL](TglPublikasi);

CREATE TABLE [PENULIS_ARTIKEL]
(
	[KdArtikel] INT FOREIGN KEY REFERENCES [ARTIKEL](KdArtikel),
	[KdPenulis] INT FOREIGN KEY REFERENCES [PENULIS](KdPenulis)
);
CREATE INDEX [idx_KdArtikel_PenulisArtikel] ON [PENULIS_ARTIKEL](KdArtikel, KdPenulis);

CREATE TABLE [SITASI] 
(
	[KdArtikel] INT FOREIGN KEY REFERENCES [ARTIKEL](KdArtikel),
	[KdArtikelRujukan] INT FOREIGN KEY REFERENCES [ARTIKEL] (KdArtikel)
);
CREATE INDEX [idx_KdArtikel_Sitasi] ON [SITASI](KdArtikel, KdArtikelRujukan);

